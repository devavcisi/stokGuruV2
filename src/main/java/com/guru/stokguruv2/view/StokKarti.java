    @Override
    public int getStokTipi() {
        Object selectedItem = cmbStokTipi.getSelectedItem();
        if (selectedItem instanceof String) {
            try {

                return Integer.parseInt((String) selectedItem);
            } catch (NumberFormatException e) {

                System.out.println("Geçersiz sayı formatı: " + selectedItem);
                return 0;
            }
        } else {

            return 0;
        }
    }

