    @Override
    public void setTarih(Timestamp time) {
        if (time != null) {

            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");

            String formattedDate = dateFormat.format(time);

            lblTarihLocal.setText(formattedDate);
        } else {

            lblTarihLocal.setText("");
        }
    }

